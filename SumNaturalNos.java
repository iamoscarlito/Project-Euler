/* 
*Solution to problem no 1
* Following is the sum of all multiples of 3 or 5 below 1000:
*/
public class SumNaturalNos {
	
 public static void main(String[] args) {
    int sum = 0;
    for (int i = 0; i < 1000; i++) {
//Finds the nos that will return zero for its Modulo operator  
        if (i % 3 == 0 || i % 5 == 0) {
            sum += i;
        }
    }
    System.out.println("Sum of all multiples of 3 or 5 below 1000 is " + sum);
	
}
}

